<div class="section-title"><h3 class="blue">Vous vous demandez comment se passe les cours ici ?
</h3></div>

<div class="section-title">

<p>En tant qu’étudiant international en échange vous devez suivre 4 ou 5 cours selon le nombre de crédits que vous demande votre école et votre formation.</p>

<p>L’UQO n’est pas considéré comme une grande école (en terme de nombre d’étudiants) donc les cours en amphithéatre sont très rares. Vous aurez souvent cours dans de grandes salles où vous serez entre 20 et 40 personnes, voir plus selon les filières.</p>

<p>Chaque cours dure environ 3h. Selon les matières, les cours peuvent s’accompagner de 2h de TD/TP. Ici, les TP/TD ne se déroulent pas forcément avec un professeur. En effet, les professeurs ont souvent un assistant(e) qui s’occupe d'effectuer la séance de TP/TD. Cette assistant(e) est en général un(e) étudiant(e) qui a déjà fait le cours et qui l’a réussi .</p>

<p>En ce qui concerne le travail à la maison, pour un cours de 3h, il est conseillé de travailler 6h chez soi. Cependant, si vous êtes plutot bon élève et que vous écoutez le cours un minimum (et oui être sur Facebook H24 ça ne vous fera malheureusement pas avancer dans la vie ) ces 6 heures peuvent être réduites à 2/3h selon mon expérience.<br> <i>Petit conseil: Essayez de faire vos fiches de révision le plus tôt possible pour ne pas être débordé de travail durant la semaine d'examen.</i></p>

<p>Vos cours dans lesquels vous êtes inscrits sont tous disponibles sur un outil appelé Moodle. Vous pouvez y retrouver tous les transparents qu’utilise les professeurs durant leur cours, mais aussi les devoirs, les TD/TP et parfois des compléments de cours. Presque tous les professeurs utilisent cette outils pour récupérer vos devoirs et pour afficher vos notes. Vous pouvez aussi voir les participants du cours(dont le professeur) pour leur envoyer des messages privés, vraiment pratique pour le coup.</p>

<h3>Et pour les notes ? Comment ça se passe?</h3><br>

<p>Il faut savoir qu’une matière est noté sur 100%. En général il y a 2 gros examens et des devoirs à rendre à côté.</p>

<h3 class="red">La première semaine d’examen :</h3><br>

<p>Cette semaine se déroule en général à la mi-octobre. C’est durant cette période que vous allez passer vos examens intras dans chaque matière. C’est à dire vos examens de mi-session. Rassurez-vous afin d'être d'attaque pour le jours J, une semaine de "relache" est prévu la semaine avant la période d'examen (pour vous permettre de reviser et non partir à Boston... Très belle ville au passage...).</p>

<h3 class="green">Les devoirs & projets<br></h3>

<p>En plus de devoir travailler sur vos cours, les professeurs peuvent vous donner des devoirs notés à faire à la maison. Par exemple, en informatique, nous pouvions avoir soit des devoirs maisons soit projets de session à rendre (ou les deux..).</p>

<h3 class="red">La semaine d’examen final<br></h3>

<p>Cette semaine se déroule juste avant la fin du trimestre. C’est là que vous allez passer vos examens finaux. Il se peut que dans certains cours, vos examens ne soit pas tous la même semaine, (1 semaine avant ou après), cela permet de souffler un peu et de bien préparer vos matières. Hélas, pour ces examens pas de semaine de relâche avant.</p>

<p>Après leur correction les professseurs établiront votre moyenne et votre pourcentage de reussite à la matière et vous saurez si vous avez échoué ou réussi.</p>

<i>Exemple de barème de notation d’une matière :<br>
<ul>
<li>Examen intra : /45 %</li>
<li>Examen final : /45 %</li>
<li>Devoirs : /15 %</li>
</ul>
</i>

<h3 class="blue">La relation étudiant/Professeur</h3>

<p>A l’université, les professeurs ont des bureaux dans lesquelles vous pouvez aller leur parler après les cours lorsque vous avez un problème ou que vous n'avez pas bien compris le cours. Il vous feront une joie de vous aider et de vous fournir des explications supplémentaires.</p>

<p>Cela dépend des professeurs, mais certains ont des heures de consultations et il faudra donc parfois organiser un rendez-vous préalablement.<br>
